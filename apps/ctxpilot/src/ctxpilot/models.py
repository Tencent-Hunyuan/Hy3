@dataclass
class SessionSummary:
    """Compact, UI-friendly view of one agent session."""
    agent: str
    session_id: str
    project_path: str | None
    started_at: str | None
    ended_at: str | None
    message_count: int
    files_touched: list[str]
    preview: str = ""  # first user message, truncated — for the UI session list
    mtime: float = 0.0  # filesystem mtime — used for change detection

    def to_dict(self) -> dict:
        return {
            "agent": self.agent,
            "session_id": self.session_id,
            "project_path": self.project_path,
            "started_at": self.started_at,
            "ended_at": self.ended_at,
            "message_count": self.message_count,
            "files_touched": list(self.files_touched),
            "preview": self.preview,
            "mtime": self.mtime,
        }


@dataclass
class ProjectView:
    """A discovered project + the agent history attached to it."""
    path: str
    is_git: bool
    branch: str | None
    recent_commits: list[str]
    sessions: list[SessionSummary] = field(default_factory=list)

    def to_dict(self) -> dict:
        return {
            "path": self.path,
            "is_git": self.is_git,
            "branch": self.branch,
            "recent_commits": list(self.recent_commits),
            "sessions": [s.to_dict() for s in self.sessions],
            "session_count": len(self.sessions),
            "agents": sorted({s.agent for s in self.sessions}),
        }
